const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const authController = require('../../controllers/auth.controller');

const router = express.Router();
// end user profile apis
//get my profile
router.route('/myprofile').get(auth('manageUsers'), userController.getMyProfile);
// update profile
router.route('/updateProfile/').post(auth('manageUsers'), userController.updateMyProfile);

//get all users
router.route('/get-users').post(auth('manageUsers'), validate(userValidation.listUsers), userController.listUsers);

// FOR BACKEND-ADMIN
router.route('/getAllUsers').post(auth('manageUsers'), userController.listAllUsers);
// FOR BACKEND-ADMIN

router
  .route('/statususer')
  .post(validate(userValidation.statusUser), userController.statususer);

router
  .route('/:userId')
  .get(auth('getUsers'), validate(userValidation.getUser), userController.getUser)
  .put(auth('manageUsers'), userController.updateUser)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser);

router
  .route('/')
  .post(auth('manageUsers'), validate(userValidation.createUser), userController.createUser)
  .get(auth('getUsers'), validate(userValidation.getUsers), userController.getUsers);


module.exports = router;

